#include <bits/stdc++.h>
using namespace std;
int main()
{

    int n;
    cout << "Enter the number\n";
    cin >> n;

    // This is (%) modulo operator

    if (n % 2 == 0)
    {
        cout << "Number is even" << endl;
    }
    else
    {
        cout << "Number is odd" << endl;
    }
}
